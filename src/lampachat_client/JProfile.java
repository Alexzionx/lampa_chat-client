    private void getHideStatus() {
        String s = message.sendServiceMessage("", "getHideMode");
        if (s.equals("hide_mode isEnabled")) {
            jLabelHideStatus.setText("ON");
            jLabelHideStatus.setForeground(Color.decode("0x00cc00"));
            jButtonSetHideMode.setText("Disable");
        } else if (s.equals("hide_mode isDisabled")) {
            jLabelHideStatus.setText("OFF");
            jLabelHideStatus.setForeground(Color.decode("0x999999"));
            jButtonSetHideMode.setText("Enable");
        }
    }

