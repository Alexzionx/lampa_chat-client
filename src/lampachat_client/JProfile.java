    private void jButtonNewPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonNewPasswordMouseClicked
        // TODO add your handling code here:

        String pass = String.valueOf(jPasswordField1.getPassword());
        if (pass.length() > 3) {
            String pass2 = String.valueOf(jPasswordField2.getPassword());
            if (pass.equals(pass2)) {
                String[] stringToSearch = {";", "\"", " ", "+", "\\"};
                boolean trig = true;
                for (String s : stringToSearch) {
                    if (pass.contains(s)) {
                        System.out.println("fail pass- " + pass);
                        trig = false;
                        new JInfoWindow().setText("Password should not contain:\n" + " ;" + " \"" + " space " + " +");
                        break;
                    }
                }
                if (trig) {
                    if (message.sendServiceMessage(pass, "setPassword").equals("ok")) {
                        Database database = new Database();
                        database.setLoginAndPass(home.getUserName(), pass);
                        new JInfoWindow(home).setText("Your password has been changed successfully.\nPlease RESTART app!");
                    } else {
                        new JInfoWindow().setText("Error password updating");
                    }
                }
            } else {
                new JInfoWindow().setText("Your password and confirmation password do not match!");
            }
        } else {
            new JInfoWindow().setText("New password is too short!\n4 characters minimum!");

        }
    }//GEN-LAST:event_jButtonNewPasswordMouseClicked

